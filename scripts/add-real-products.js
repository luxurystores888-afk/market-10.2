async function addProducts() {
  console.log('üöÄ Starting to add 50 real products...\n');

  try {
    let added = 0;
    
    for (const product of realProducts) {
      try {
        await db.insert(products).values({
          ...product,
          createdAt: new Date(),
          updatedAt: new Date(),
        });
        
        added++;
        console.log(`‚úÖ Added: ${product.name} ($${product.price})`);
      } catch (error) {
        console.error(`‚ùå Failed to add ${product.name}:`, error.message);
      }
    }

    console.log(`\nüéâ Successfully added ${added}/${realProducts.length} products!`);
  } catch (error) {
    console.error('\n‚ùå Error:', error);
  }
}

